#include <stdio.h>

int main(){
    printf("a = %d, b = %d, sum = a + b = %d + %d = % d\n", 1, 2, 1, 2, 1+2);

    printf("a = %d, b = %d, sum = a - b = %d - %d = % d\n", 2, 1, 2, 1, 2-1);

    printf("a = %d, b = %d, multiply = a * b = %d * %d = % d\n", 1, 2, 1, 2, 1*2);

    printf("a = %d, b = %d, divide = a / b = %d / %d = % d\n", 4, 2, 4, 2, 4/2);

    printf("%d %% %d = %d \n", 5, 2, 5%2);
    
}